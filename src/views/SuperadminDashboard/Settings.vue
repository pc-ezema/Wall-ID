<template>
  <DashboardSidebar />

  <section class="main_content dashboard_part large_header_bg">
    <DashboardNavbar />

    <!--Main Content-->
    <div class="main_content_iner overly_inner">
      <div class="container-fluid p-0">
        <!--Breadcrumb-->
        <div class="row">
          <div class="col-12">
            <div class="dashboard_header mb-3">
              <div class="row">
                <div class="col-lg-12">
                  <div class="dashboard_header_title">
                    <h3>Edit Profile</h3>
                  </div>
                </div>
              </div>
            </div>
          </div>
        </div>

        <!--Profile Details-->
        <div class="row pd-section secForm editProfileDetail">
          <div class="col-lg-6 mt-3">
            <div class="row justify-content-center">
                <form @submit.prevent="update_profile()">
                    <h4>Update Profile</h4>
                    <hr />
                    <div class="col-12 mb-3">
                    <label>Name</label>
                    <input
                        type="text"
                        class="input"
                        v-model="name"
                        placeholder="Enter Name"
                    />
                    </div>
                    <div class="col-12 mb-3">
                    <label>Email</label>
                    <input
                        type="email"
                        class="input"
                        v-model="email"
                        placeholder="Enter email"
                    />
                    </div>
                    <!--Button-->
                    <div class="col-12 text-center mb-3">
                    <button
                        v-if="$wait.is('processing')"
                        type="submit"
                        class="button"
                    >
                        Profile Updating...
                    </button>
                    <button v-else type="submit" class="button">
                        Update Profile
                    </button>
                    </div>
                </form>
            </div>
          </div>
          <div class="col-lg-6 mt-3">
            <div class="row justify-content-center">
                <form @submit.prevent="update_password(id)">
                    <h4>Change Password</h4>
                    <hr />
                    <!--New Password-->
                    <div class="col-12 mb-3">
                    <label>New Password</label>
                    <input
                        type="password"
                        class="input"
                        v-model="new_password"
                        placeholder="Enter New Password"
                    />
                    </div>
                    <!--Confirm New Password-->
                    <div class="col-12 mb-3">
                    <label>Confirm New Password</label>
                    <input
                        type="password"
                        class="input"
                        v-model="new_password_confirmation"
                        placeholder="Re-Enter Password"
                    />
                    </div>
                    <!--Button-->
                    <div class="col-12 text-center mb-3">
                    <button type="submit" class="button">
                        Update Password
                    </button>
                    </div>
                </form>
            </div>
          </div>
        </div>
      </div>
    </div>

    <DashboardFooter />
  </section>

  <div id="back-top" style="display: none">
    <a title="Go to Top" href="#">
      <i class="ti-angle-up"></i>
    </a>
  </div>
</template>

<style scoped src="@/assets/css/styleDashboard.css"></style>
<script>
import DashboardSidebar from "./DashboardSidebar.vue";
import DashboardNavbar from "./DashboardNavbar.vue";
import DashboardFooter from "./DashboardFooter.vue";

import axios from "axios";

export default {
  components: { DashboardSidebar, DashboardNavbar, DashboardFooter },
  data() {
    return {
      baseURL: axios.defaults.baseURL.slice(0, -5),
      id: null,
      name: null,
      email: null,
      new_password: null,
      new_password_confirmation: null,
      user: JSON.parse(localStorage.getItem('user')) || [],
    };
  },

  methods: {
    async update_profile() {
      this.$wait.start("processing");
      this.$Progress.start();

      await axios
        .post("admin/profile/update/" + this.id, {
          name: this.name,
          email: this.email,
        })
        .then((response) => {
          this.$wait.end("processing");
          this.$Progress.finish();
          this.$notify({
            type: "success",
            title: response.data.message,
            duration: 5000,
            speed: 1000,
          });
        })
        .catch((error) => {
          if (error.response.status == 400) {
            this.$wait.end("processing");
            this.$Progress.fail();
            for (let i in error.response.data.errors) {
              this.$notify({
                type: "error",
                title: error.response.data.errors[i][0],
                duration: 5000,
                speed: 1000,
              });
            }
          } else {
            this.$wait.end("processing");
            this.$Progress.fail();
            this.$notify({
              type: "error",
              title: error.response.data.message,
              duration: 5000,
              speed: 1000,
            });
          }
        });
    },

    async update_password() {
      this.$Progress.start();

      await axios
        .post("admin/password/update/" + this.id, {
          new_password: this.new_password,
          new_password_confirmation: this.new_password_confirmation,
        })
        .then((response) => {
          this.$Progress.finish();
          this.$notify({
            type: "success",
            title: response.data.message,
            duration: 5000,
            speed: 1000,
          });
          this.new_password = null;
          this.new_password_confirmation = null;
        })
        .catch((error) => {
          if (error.response.status == 400) {
            this.$Progress.fail();
            for (let i in error.response.data.errors) {
              this.$notify({
                type: "error",
                title: error.response.data.errors[i][0],
                duration: 5000,
                speed: 1000,
              });
            }
          } else {
            this.$wait.end("processing");
            this.$Progress.fail();
            this.$notify({
              type: "error",
              title: error.response.data.message,
              duration: 5000,
              speed: 1000,
            });
          }
        });
    },
  },

  mounted() {   
    this.id = this.user.id;
    this.name = this.user.name;
    this.email = this.user.email;
    window.scrollTo(0, 0);
  },
};
</script>
