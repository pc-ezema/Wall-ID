<template>
  <div class="container-fluid no-gutters">
    <div class="row">
      <div class="col-lg-12 p-0">
        <div
          class="header_iner d-flex justify-content-between align-items-center"
        >
          <div class="sidebar_icon d-lg-none">
            <i class="ti-menu"></i>
          </div>
          <div class="line_icon open_miniSide d-none d-lg-block">
            <img src="@/assets/img/line_img.png" style="cursor: pointer" />
          </div>
          <span>Individual Account</span>
          <div
            class="header_right d-flex justify-content-between align-items-center"
          >
            <div class="header_notification_warp d-flex align-items-center">
              <li>
                <a class="bell_notification_clicker" href="#">
                  <img src="@/assets/img/icon/bell.svg" alt="" />
                  <span v-if="loading">

                  </span>
                  <span v-else>
                    {{this.countNotification}}
                  </span>
                </a>
                <div class="Menu_NOtification_Wrap">
                  <div class="notification_Header">
                    <h4>Notifications <span style="float: right">View More</span></h4>
                  </div>
                  <div class="Notification_body">
                    <div v-if="!viewUnreadNotification || !viewUnreadNotification.length" class="single_notify d-flex align-items-center justify-content-center">
                      <div v-if="loading" class="notify_content">
                        <div style="text-align: center"  class="fa-3x">
                            <i class="fas fa-spinner fa-spin"></i>
                        </div>
                      </div>
                      <div v-else class="notify_content">
                        <div style="text-align: center"  class="fa-3x">
                            <h5>No New Notification</h5>
                        </div>
                      </div>
                    </div>
                    <div v-else class="single_notify d-flex align-items-center">
                        <div v-for="(row, index) in viewUnreadNotification" v-bind:key="index" class="notify_content p-2" style="background: azure">
                          <a href="javascript:void(0)" @click="readNotification(row.id)">
                            <h5 style="font-weight: 700">{{row.subject}} - {{row.firstname}}</h5>
                            <p>{{row.message}}</p>
                          </a>
                        </div>
                    </div>
                  </div>
                </div>
              </li>
            </div>
            <div class="profile_info">
              <img
                v-if="user.details.image"
                v-bind:src="this.baseURL + user.details.image"
                alt="#"
              />
              <img v-else src="@/assets/img/client_img.png" alt="#" />
              <div class="profile_info_iner">
                <div class="profile_author_name">
                  <p>Individual</p>
                  <h5>
                    {{ user.details.firstname }}
                    {{ user.details.lastname }}
                  </h5>
                </div>
                <div class="profile_info_details">
                  <router-link to="/individual-dashboard/my-profile"
                    ><a>My Profile</a></router-link
                  >

                  <router-link to="/individual-dashboard/settings"
                    ><a>Settings</a></router-link
                  >
                  <a href="javascript:void(0)" @click="logoutClick">Log Out</a>
                </div>
              </div>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<style scoped src="@/assets/css/styleDashboard.css"></style>
<script>
import axios from "axios";

export default {
  data() {
    return {
      baseURL: axios.defaults.baseURL.slice(0, -5),
      user: JSON.parse(localStorage.getItem('user')) || [],
      countNotification: {},
      viewUnreadNotification: [],
      loading: false
    };
  },

  methods: {
    logoutClick() {
      localStorage.removeItem("token", "user");
      axios.post("logout");
      this.$router.replace("/");
    },
    
    countUnreadNotification() {
      this.loading = true;
      axios
        .get("users/user/countUnreadNotification", {
          headers: {
            Authorization: `Bearer ${localStorage.getItem("token")}`,
          },
        })
        .then((response) => {
          this.loading = false;
          this.countNotification = response.data.data;
        })
        .catch((error) => {
          this.loading = false;
          console.log(error);
        });
    },

    viewUnreadNotifications() {
      this.loading = true;
      axios
        .get("users/user/getUnreadNotifications", {
          headers: {
            Authorization: `Bearer ${localStorage.getItem("token")}`,
          },
        })
        .then((response) => {
          this.loading = false;
          // console.log(response.data.data);
          this.viewUnreadNotification = response.data.data;
        })
        .catch((error) => {
          this.loading = false;
          console.log(error);
        });
    },

    async readNotification(data) {
      this.$Progress.start();
      await axios
        .get(
          "users/user/readNotification/" + data,
          {
            status: "Approved",
          },
          {
            headers: {
              Authorization: `Bearer ${localStorage.getItem("token")}`,
            },
          }
        )
        .then((response) => {
          this.$Progress.finish();
          // this.$notify({
          //   type: "success",
          //   title: response.data.message,
          //   duration: 5000,
          //   speed: 1000,
          // });
          this.countUnreadNotification();
          this.viewUnreadNotifications();
        })
        .catch((error) => {
          this.$Progress.fail();
          // this.$notify({
          //   type: "error",
          //   title: error.response.data.message,
          //   duration: 5000,
          //   speed: 1000,
          // });
        });
    },
  },

  mounted() {
    this.countUnreadNotification();
    this.viewUnreadNotifications();
    window.scrollTo(0, 0);
    let externalScriptJquery = document.createElement("script");
    let externalScriptMetisMenu = document.createElement("script");
    let externalScriptCustom = document.createElement("script");

    externalScriptJquery.setAttribute(
      "src",
      "https://ajax.googleapis.com/ajax/libs/jquery/3.5.1/jquery.min.js"
    );
    externalScriptMetisMenu.setAttribute(
      "src",
      "https://cdn.statically.io/gh/NathTimi/Mart-script/main/metisMenu.js"
    );
    externalScriptCustom.setAttribute(
      "src",
      "https://cdn.statically.io/gh/NathTimi/Mart-script/main/custom.js"
    );

    document.head.appendChild(externalScriptJquery);
    document.head.appendChild(externalScriptMetisMenu);
    document.head.appendChild(externalScriptCustom);
  },
};
</script>
